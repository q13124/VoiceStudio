def get_all_panels() -> list[PanelInfo]:
    """Get flat list of all panels."""
    panels = []
    for category_panels in PANELS_BY_CATEGORY.values():
        panels.extend(category_panels)
    return panels


def get_panels_by_category(category: str) -> list[PanelInfo]:
    """Get panels for a specific category."""
    return PANELS_BY_CATEGORY.get(category, [])


def get_panel_by_name(name: str) -> PanelInfo | None:
    """Get panel info by name."""
    for panel in get_all_panels():
        if panel.name == name:
            return panel
    return None


def get_automation_id(panel_name: str, element: str) -> str:
    """Generate automation ID for a panel element."""
    return f"{panel_name}View_{element}"


